disp('++++++++++++++++++++++++++++++')
disp('例 4.4（数式処理）')
disp('++++++++++++++++++++++++++++++')

clear
format compact

syms s

A = [ -3  1
       2 -2 ];
b = [ 2
      0 ];
c = [ 0  1 ];

disp(' ')
disp('___ ステップ 1 __________________________')

disp(' ')
disp('--- A の特性多項式 ---------')
det_A = det(s*eye(2) - A)

disp(' ')
disp('--- A の特性多項式：λ^2 + a1*λ + a0 ---------')
a0 = subs(det_A, s, 0)
a1 = subs(diff(det_A, s), s, 0)

disp(' ')
disp('___ ステップ 2 __________________________')

disp(' ')
disp('--- 可制御行列 Vc ---------')
Vc = ctrb(A,b);
Vc = sym(Vc)

disp(' ')
disp('--- Mc ---------')
Mc = [ a1  1
       1   0 ]

disp(' ')
disp('___ ステップ 3 __________________________')

disp(' ')
disp('--- 可制御標準形への変換行列 Tc ---------')
Tc = inv(Vc*Mc)


disp(' ')
disp('___ ステップ 4 __________________________')

disp(' ')
disp('--- 可制御標準形 ---------')
Ac = Tc*A*inv(Tc)
bc = Tc*b
cc = c*inv(Tc)


