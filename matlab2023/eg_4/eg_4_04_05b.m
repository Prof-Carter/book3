disp('++++++++++++++++++++++++++++++')
disp('例 4.5（数式処理）')
disp('++++++++++++++++++++++++++++++')

disp(' ')
disp('--- 配置したい固有値の設定 ---------')
p = [ -8+4j
      -8-4j ]

disp(' ')
disp('--- Δ(λ) = λ^2 + d1*λ + d0  ---------')
delta = (s - p(1))*(s - p(2))

d0 = subs(delta, s, 0)
d1 = subs(diff(delta, s), s, 0)

disp(' ')
disp('--- u(t) = kc*xc(t) ---------')
kc = [ a0-d0  a1-d1 ]

disp(' ')
disp('--- u(t) = k*x(t) ---------')
k = kc*Tc

disp(' ')
disp('--- A + b*k の固有値 ---------')
eig(A + b*k)
